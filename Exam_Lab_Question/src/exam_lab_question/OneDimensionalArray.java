package exam_lab_question;
public class OneDimensionalArray{
    public static void main(String[] args) {
        int a[] = new int[3];
        a[0] = 5;
        a[1] = 4;
        a[2] = 3;
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }

}


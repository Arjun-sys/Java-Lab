package lab2;
public class SingleDimensionalArray {

    public static void main(String[] args) {
        int a[] = new int[5];
        a[0] = 5;
        a[1] = 4;
        a[2] = 3;
        a[3] = 2;
        a[4] = 1;
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }

}
